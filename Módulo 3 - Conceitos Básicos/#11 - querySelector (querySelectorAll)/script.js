/**
 * O querySelector é utilizado para retornar o primeiro elemento, classe, id, ou a junção de um elemento + class ou elemento + id.
 * 
 * 
 * */ 

// exemplos: 

document.querySelector('.lista'); // retorna o primeiro elemento com a classe lista

document.querySelector('ol.lista'); // retorna o primeiro elemento ol com a classe lista

document.querySelector('#titulo'); // retorna o primeiro elemento com o id titulo



/**
 * O querySelectorAll é utilizado para retornar todos os elemento, classe, id, ou a junção de um elemento + class ou elemento + id em uma NodeList.
 * 
 * 
 * */ 

// exemplos: 

document.querySelectorAll('.lista'); // retorna todos os elementos com a classe lista

document.querySelectorAll('li'); // retorna todos os elementos div