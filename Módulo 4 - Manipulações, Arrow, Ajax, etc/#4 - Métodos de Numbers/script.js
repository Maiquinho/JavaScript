/**
 * 
 * O Método .toString() transforma um valor numérico em uma string.
 * 
 * 
 * O Método toFixed() formata um número de acordo com o valor de casas decimais especificado pelo parâmetro.
 * 
 * O parseInt(x) transforma o valor especificado no parâmetro em um inteiro.
 * 
 * 
 * O parseFloat(x) transforma o valor especificado no parâmetro em um número de ponto flutuante.
 * 
 * 
 * 
*/

